uint8_t rotaryEnc(void) // CW=1,CCW=2
{
  uint8_t nowEnc = 0;
  uint8_t returnEnc = 0;
  static uint8_t oldEnc = 0;

  nowEnc = (SWB << 1) + SWA;

  if(oldEnc == 0 && nowEnc == 1){ // IF CW
      returnEnc = 1;
  } else if(oldEnc == 0 && nowEnc == 2){ //IF CCW
      returnEnc = 2;
  }
  oldEnc = nowEnc;

  return returnEnc;
}

void menuCalc(void)
{
	//CW
	if((CursorLine < (MenuItems-1)) && (enc == 1)){
		CursorLine++;
	} else if((CursorLine >= (MenuItems-1)) && (enc == 1)) {
		CursorLine = 0;
	}
	//CCW
	if((CursorLine > 0) && (enc == 2)){
		CursorLine--;
	} else if((CursorLine <= 0) && (enc == 2)) {
		CursorLine = MenuItems-1;
	}

}

